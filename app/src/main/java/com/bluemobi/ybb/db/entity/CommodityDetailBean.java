public class CommodityDetailBean implements Serializable
{

    private String comboName;
    private String attributeName;
    private String customerPrice;
    private String imagePath;
    private String productId;
    private String reserveTime;

    private int type; //0 病患  1 其他

    private int sumAddShopCar;

    public String getComboName() {
        return comboName;
    }

    public void setComboName(String comboName) {
        this.comboName = comboName;
    }

    public String getAttributeName() {
        return attributeName;
    }

    public void setAttributeName(String attributeName) {
        this.attributeName = attributeName;
    }

    public String getCustomerPrice() {
        return customerPrice;
    }

    public void setCustomerPrice(String customerPrice) {
        this.customerPrice = customerPrice;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public int getSumAddShopCar() {
        return sumAddShopCar;
    }

    public void setSumAddShopCar(int sumAddShopCar) {
        this.sumAddShopCar = sumAddShopCar;
    }

    public String getReserveTime() {
        return reserveTime;
    }

    public void setReserveTime(String reserveTime) {
        this.reserveTime = reserveTime;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
