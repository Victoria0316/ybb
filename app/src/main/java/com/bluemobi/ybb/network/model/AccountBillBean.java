package com.bluemobi.ybb.network.model;

import java.io.Serializable;
import java.util.List;

/**
 * Created by gaoyn on 2015/8/13.
 */
public class AccountBillBean implements Serializable{

    private String currentnum; //每页条数
    private String currentpage; //当前页数
    private String pageTime; //分页时间

    private List<AccountBillInfo> info;

    public String getCurrentnum() {
        return currentnum;
    }

    public void setCurrentnum(String currentnum) {
        this.currentnum = currentnum;
    }

    public String getCurrentpage() {
        return currentpage;
    }

    public void setCurrentpage(String currentpage) {
        this.currentpage = currentpage;
    }

    public String getPageTime() {
        return pageTime;
    }

    public void setPageTime(String pageTime) {
        this.pageTime = pageTime;
    }

    public List<AccountBillInfo> getInfo() {
        return info;
    }

    public void setInfo(List<AccountBillInfo> info) {
        this.info = info;
    }
}
