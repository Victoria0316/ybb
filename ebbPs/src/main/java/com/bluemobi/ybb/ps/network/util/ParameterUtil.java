public class ParameterUtil {

    public static String getSmsType(com.bluemobi.ybb.ps.network.util.SmsType smsType){
        String temp = "";
        if(com.bluemobi.ybb.ps.network.util.SmsType.REGISTER == smsType){
            temp = "register";
        }else if(com.bluemobi.ybb.ps.network.util.SmsType.LOGIN == smsType){
            temp = "login";
        }else if(com.bluemobi.ybb.ps.network.util.SmsType.BIND == smsType){
            temp = "bind";
        }else if(com.bluemobi.ybb.ps.network.util.SmsType.FORGOTPASSWORD == smsType){
            temp = "forgotpassword";
        }
        return temp;
    }


}
