package com.bluemobi.ybb.ps.network.model;

import java.io.Serializable;

/**
 * Created by gaoyn on 2015/8/18.
 */
public class MineBean implements Serializable {

    private String userId; //用户ID
    private String nickName;//昵称
    private String headPicUrl; //头像地址
    private String distributionCount;//餐品配送数量
    private String avgComment;//平均评价
    private String praiseCount;//好评率
    private String rank;//排名
    private String totalAmount; //配送总额度

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public String getHeadPicUrl() {
        return headPicUrl;
    }

    public void setHeadPicUrl(String headPicUrl) {
        this.headPicUrl = headPicUrl;
    }

    public String getDistributionCount() {
        return distributionCount;
    }

    public void setDistributionCount(String distributionCount) {
        this.distributionCount = distributionCount;
    }

    public String getAvgComment() {
        return avgComment;
    }

    public void setAvgComment(String avgComment) {
        this.avgComment = avgComment;
    }

    public String getPraiseCount() {
        return praiseCount;
    }

    public void setPraiseCount(String praiseCount) {
        this.praiseCount = praiseCount;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(String totalAmount) {
        this.totalAmount = totalAmount;
    }
}
